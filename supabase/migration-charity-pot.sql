--
-- Идея (как у подушки/накоплений, но категория «Благотворительность»):
--   Пополнение копилки     = расход в категории «Благотворительность»
--                            (paid_from_pot = NULL) -- это отложенные 5%.
--   Снятие (пожертвование) = расход с пометкой paid_from_pot = 'charity'
--                            -- баланс копилки уменьшается.
--
-- Благотворительность НЕ входит в «Уже отложено» (личные накопления):
-- это деньги «не мои», поэтому считаются отдельной копилкой.
--
-- Расширяем разрешённые значения paid_from_pot, добавляя 'charity'.

alter table public.expenses
  drop constraint if exists expenses_paid_from_pot_check;
alter table public.expenses
  add constraint expenses_paid_from_pot_check
  check (paid_from_pot in ('cushion', 'free', 'charity'));
